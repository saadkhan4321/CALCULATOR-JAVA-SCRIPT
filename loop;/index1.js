function add(num1,num2){
    document.write(num1+num2)
}
function sub(num1,num2){
    document.write(num1-num2)
}
function mult(num1,num2){
    document.write(num1*num2)
}
function divide(num1,num2){
    document.write(num1/num2)
}
function reminder(num1,num2){
    document.write(num1%num2)
}